    private boolean faltanDatos(){
        return txtCaravana.getText().equals("") || txtPeso.getText().equals("");
    }
    private boolean existe(int caravanaVaca){
        Vaca vaca;
        vaca = controladora.buscarVaca(caravanaVaca);
        return vaca != null;
    }
    private void cargarDdlCategoria() throws SQLException{
        ModeloCbCategoria categorias = new ModeloCbCategoria(controladora.listarCategorias());
        this.ddlCategoria.setModel(categorias);
        this.ddlCategoria.setSelectedIndex(-1);
    }
    private void cargarDdlRaza() throws SQLException{
        ModeloCbRaza razas = new ModeloCbRaza(controladora.listarRazas());
        this.ddlRaza.setModel(razas);
        this.ddlRaza.setSelectedIndex(-1);
    }
    private void limpiar(){
        this.txtCaravana.setText("");
        this.txtPeso.setText("");
        this.ddlCategoria.setSelectedIndex(-1);
        this.ddlRaza.setSelectedIndex(-1);
        setFechaActualCalendar();
    }
    private void cargarTabla(){
        List<Vaca> vacas = controladora.listarVacas();
        modeloTabla = new ModeloTblVacas(vacas);
        tblVacas.setModel(modeloTabla);
    }
    private void setFechaActualCalendar(){
        fechaActual = new Date();
        clFecha.setDate(fechaActual);
        
    }
