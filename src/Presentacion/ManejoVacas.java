    private class ListenerCliente implements ListSelectionListener {
        @Override
        public void valueChanged(ListSelectionEvent e) {
            int seleccion = tblVacas.getSelectedRow();
            if (seleccion != -1) {
                vacaSeleccionada = modeloTabla.getVaca(seleccion);
                cargarDatosVaca();
                btnGuardarCambios.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnCancelar.setEnabled(true);
                btnAgregarNuevo.setEnabled(false);
                ddlRaza.setEnabled(false);
                ddlCategoria.setEnabled(false);
            }
        }
    }
    private void cargarDatosVaca(){
        this.txtCaravana.setText(vacaSeleccionada.getCaravana());
        this.txtPeso.setText(vacaSeleccionada.getPeso().toString());
        
        this.ddlCategoria.setSelectedItem(ABORT);
        this.ddlRaza.setSelectedItem(ABORT); //ELEGIR EL TIPO
    }
