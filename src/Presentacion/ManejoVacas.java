    private void btnAgregarNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarNuevoActionPerformed
        if(!faltanDatos()){
            if(!existe(Integer.parseInt(txtCaravana.getText()))){//un int no da para el RUT
                Vaca vaca = new Vaca();
                Categoria categoria = new Categoria();
                Raza raza = new Raza();
                vaca.setCaravana(txtCaravana.getText());
                vaca.setPeso(Integer.parseInt(txtPeso.getText()));
                vaca.setFechaNacimiento(fechaActual);
                categoria.setId(ddlCategoria.getSelectedIndex()+1);
                vaca.setCategoria(categoria);
                raza.setId(ddlRaza.getSelectedIndex()+1);
                vaca.setRaza(raza);

                controladora.altaVaca(vaca);
                limpiar();
                cargarTabla();
            }else{
                //sout : si estado = 0 { cambiar estado en la bbdd a 1}
            }
        }else{
            this.lblMensaje.setText("Faltan ingresar datos");
        }
    }//GEN-LAST:event_btnAgregarNuevoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        limpiar();
        btnAgregarNuevo.setEnabled(true);
        btnGuardarCambios.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        ddlCategoria.setEnabled(true);
        ddlRaza.setEnabled(true);
        cargarTabla();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        int seleccion = tblVacas.getSelectedRow();
        if (seleccion != -1) {
            Vaca unaVaca = new Vaca();
            unaVaca.setId(vacaSeleccionada.getId());
            unaVaca.setCaravana(txtCaravana.getText());
            unaVaca.setPeso(Integer.parseInt(txtPeso.getText()));
            controladora.modificarVaca(unaVaca);
        }
        limpiar();
        cargarTabla();
        btnAgregarNuevo.setEnabled(true);
        btnGuardarCambios.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        ddlCategoria.setEnabled(true);
        ddlRaza.setEditable(true);
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int seleccion = tblVacas.getSelectedRow();
        if (seleccion != -1) {
            controladora.bajaVaca(vacaSeleccionada.getId());
        }
        cargarTabla();
        btnAgregarNuevo.setEnabled(true);
        btnGuardarCambios.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        ddlCategoria.setEnabled(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

